package gremlins;

/**
 * This class represents a door.
 */
public class Door {
    /**
     * x coordinate of the door
     */
    public int doorX;
    /**
     * y coordinate of the door
     */
    public int doorY;

    /**
     * constructor for the class Door
     *
     * @param x x coordinate of the door
     * @param y y coordinate of the door
     */
    public Door(int x, int y) {
        doorX = x;
        doorY = y;
    }
}
